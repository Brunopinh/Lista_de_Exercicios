# 6. Escreva um programa que peça um número inteiro e informe se ele é positivo,
#    negativo ou zero.

numero = int(input("Digite um numero inteiro: "))

if numero > 0:
    print("Esse numero é positivo")

elif numero < 0:
    print("Esse numero é Negativo")

else: 
    print("Esse numero é 0")    
